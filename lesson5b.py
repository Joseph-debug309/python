#Functions with parameters
#Parameters: these are values that get passed as arguments given to a function inside the parenthesis.

def greetings(name):
    print(f"{name} How are you? Hope everything is fine")

greetings("Maina")

#Create a function that accepts parameters to add two numbers
def add(x, y):
    num1 = x 
    num2 = y
    sum = num1 + num2
    print(sum)

add(5, 13)